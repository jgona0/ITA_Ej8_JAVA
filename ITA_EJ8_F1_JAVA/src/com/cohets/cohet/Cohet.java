package com.cohets.cohet;

public class Cohet {

	// Variables de clase
	private String id;
	private int num_props;
	

	// Constructor
	public Cohet(String id, int num_props) {
		
		this.id = id;
		this.num_props = num_props;
		
	}

	
	// GETTERS
	public String getId() {
		return id;
	}
	
	public int getNumProps() {
		return num_props;
	}
	


}
